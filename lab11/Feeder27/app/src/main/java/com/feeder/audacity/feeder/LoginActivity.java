    /**
     * Represents an asynchronous login/registration task used to authenticate
     * the user.
     */
    public class UserLoginTask extends AsyncTask<Void, Void, Boolean> {

        private int status;

        private final String mUsername;
        private final String mPassword;

        LoginActivity context;

        UserLoginTask(String username, String password, final LoginActivity context) {
            mUsername = username;
            mPassword = password;
            this.context = context;
        }

        @Override
        protected Boolean doInBackground(Void... params) {
            // TODO: attempt authentication against a network service.

            String loginUrl = Constants.LOGIN;
            CookieManager cookieManager = CookieManager.getInstance();
            try {
                URL obj1 = new URL(loginUrl);
                HttpURLConnection con1 = (HttpURLConnection) obj1.openConnection();
                con1.setRequestMethod("GET");

                String csrf=null;
                int status = con1.getResponseCode();
                Log.d("status",""+status);
                if(status==HttpURLConnection.HTTP_OK){
                    csrf = con1.getHeaderField("Set-Cookie").split(";\\s*")[0];
                    cookieManager.setCookie(Constants.DOMAIN,con1.getHeaderField("Set-Cookie") ) ;
                }

                URL obj2 = new URL(loginUrl);
                HttpURLConnection con2 = (HttpURLConnection) obj2.openConnection();
                String token =csrf.split("=")[1];
                con2.setRequestMethod("POST");
                con2.setRequestProperty("Cookie", cookieManager.getCookie(Constants.DOMAIN));
//                con2.setRequestProperty("X-CSRFToken",token);
                String loginDetails = "csrfmiddlewaretoken="+token+"&username="+mUsername+"&password="+mPassword;
                DataOutputStream wr = new DataOutputStream(con2.getOutputStream());
                wr.writeBytes(loginDetails);wr.flush();wr.close();
                int responseCode = con2.getResponseCode();
                BufferedReader in = new BufferedReader(
                        new InputStreamReader(con2.getInputStream()));
                String inputLine;
                StringBuffer response = new StringBuffer();

                while ((inputLine = in.readLine()) != null) {
                    response.append(inputLine);
                }
                in.close();
                
                Log.d("response code ", ""+responseCode);
                Log.d("response", response.toString());
                try {
                    Log.d("cookie", con2.getHeaderField("Set-Cookie"));
                    cookieManager.setCookie(Constants.DOMAIN, con2.getHeaderField("Set-Cookie"));
                    editor.putString("cookie",con2.getHeaderField("Set-Cookie").toString());
                    editor.commit();
                }
                catch (NullPointerException e) {
                    return false;
                }
                
                Thread.sleep(20);
            } catch (IOException e) {
                status = Constants.NETWORKERR;
                return false;
            } catch (InterruptedException e) {
                status = Constants.NOTOKAY;
                return false;
            }

            status = Constants.LOGINOK;
            return true;
        }

        @Override
        protected void onPostExecute(final Boolean success) {
            mAuthTask = null;
            showProgress(false);

            if (success) {
                Intent intent = new Intent(context,MainActivity.class);
                startActivity(intent);
                finish();
            }
            else if (status == Constants.NETWORKERR ){
                Log.d("DEB", "Network manager");
                Toast.makeText(context,"Check Network Connection",Toast.LENGTH_SHORT).show();
            }
            else {
                mPasswordView.setError(getString(R.string.error_incorrect_password));
                mPasswordView.requestFocus();
            }
        }

        @Override
        protected void onCancelled() {
            mAuthTask = null;
            showProgress(false);
        }
    }
